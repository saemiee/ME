import UIKit

extension UIColor {
    static let background = UIColor(resource: .background)
    static let darkGray = UIColor(resource: .darkGray)
    static let gray = UIColor(resource: .gray)
    static let lightGray = UIColor(resource: .lightGray)
    static let yellow = UIColor(resource: .yellow)
    static let lightOrange = UIColor(resource: .lightOrange)
}

